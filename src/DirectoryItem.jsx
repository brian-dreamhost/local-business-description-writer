export default function DirectoryItem({ directory, status, onStatusChange }) {
  const currentStatus = STATUS_OPTIONS.find(s => s.value === status) || STATUS_OPTIONS[0]

  const cycleStatus = () => {
    const currentIndex = STATUS_OPTIONS.findIndex(s => s.value === status)
    const nextIndex = (currentIndex + 1) % STATUS_OPTIONS.length
    onStatusChange(directory.id, STATUS_OPTIONS[nextIndex].value)
  }

  return (
    <div className="flex items-center gap-2 p-3 rounded-xl bg-midnight/30 border border-metal/10 hover:border-metal/30 transition-colors">
      {/* Checkbox area - padded for 44px touch target */}
      <button
        onClick={cycleStatus}
        className="shrink-0 flex items-center justify-center min-h-[44px] min-w-[44px] rounded-lg focus:outline-none focus:ring-2 focus:ring-azure focus:ring-offset-2 focus:ring-offset-abyss"
        aria-label={`Toggle status for ${directory.name}: currently ${currentStatus.label}`}
      >
        <span className={`w-6 h-6 rounded-md border-2 flex items-center justify-center transition-colors ${
          status === 'listed'
            ? 'bg-turtle border-turtle'
            : status === 'needs-update'
            ? 'bg-tangerine/20 border-tangerine'
            : 'bg-transparent border-metal/40 hover:border-metal/60'
        }`}>
          {status === 'listed' && (
            <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={2.5} stroke="currentColor" className="w-4 h-4 text-abyss">
              <path strokeLinecap="round" strokeLinejoin="round" d="m4.5 12.75 6 6 9-13.5" />
            </svg>
          )}
          {status === 'needs-update' && (
            <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={2.5} stroke="currentColor" className="w-4 h-4 text-tangerine">
              <path strokeLinecap="round" strokeLinejoin="round" d="M12 9v3.75m-9.303 3.376c-.866 1.5.217 3.374 1.948 3.374h14.71c1.73 0 2.813-1.874 1.948-3.374L13.949 3.378c-.866-1.5-3.032-1.5-3.898 0L2.697 16.126ZM12 15.75h.007v.008H12v-.008Z" />
            </svg>
          )}
        </span>
      </button>

      {/* Icon + Name */}
      <DirectoryIcon type={directory.icon} />
      <div className="min-w-0 flex-1">
        <div className="flex items-center gap-2 flex-wrap">
          <span className="text-sm font-medium text-white truncate">{directory.name}</span>
          <span className={`text-xs px-2 py-0.5 rounded-full ${currentStatus.bg} ${currentStatus.color}`}>
            {currentStatus.label}
          </span>
        </div>
        <div className="flex items-center gap-3 mt-0.5">
          <span className="text-xs text-galactic">{directory.charLimit} char limit</span>
          <a
            href={directory.url}
            target="_blank"
            rel="noopener noreferrer"
            className="text-xs text-azure hover:text-white transition-colors py-1"
          >
            Create/Edit Listing
          </a>
        </div>
      </div>
    </div>
  )
}
